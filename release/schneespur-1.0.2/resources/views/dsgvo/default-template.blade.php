## 2. Zweck der Datenverarbeitung

Im Rahmen Ihrer Tätigkeit im Winterdienst werden folgende Daten erhoben und verarbeitet:

- **GPS-Standortdaten** während der Einsatzzeiten zur Dokumentation der durchgeführten Räum- und Streueinsätze
- **Einsatzzeiten** (Beginn und Ende) zur Arbeitszeiterfassung
- **Fahrzeugzuordnung** zur Nachvollziehbarkeit der eingesetzten Fahrzeuge
- **Fotos und Notizen** als Beweissicherung der durchgeführten Arbeiten

## 3. Rechtsgrundlage

Die Verarbeitung erfolgt auf Grundlage von **Art. 6 Abs. 1 lit. f DSGVO** (berechtigtes Interesse). Das berechtigte Interesse des Arbeitgebers liegt in der Erfüllung der Verkehrssicherungspflicht und dem Nachweis ordnungsgemäß durchgeführter Winterdiensteinsätze.

## 4. Erfassung der GPS-Daten

Die GPS-Erfassung erfolgt über die App **OwnTracks** auf Ihrem Dienstgerät. Die Standortdaten werden ausschließlich während der aktiven Einsatzzeiten aufgezeichnet und an den Server des Arbeitgebers übermittelt.

## 5. Speicherdauer

Die erhobenen Daten werden für die Dauer der gesetzlichen Aufbewahrungsfristen gespeichert (in der Regel 3 Jahre gemäß den Verjährungsfristen für Verkehrssicherungspflichten). Nach Ablauf der Fristen werden die Daten gelöscht oder anonymisiert.

## 6. Ihre Rechte

Sie haben gemäß DSGVO folgende Rechte:

- **Auskunftsrecht** (Art. 15 DSGVO): Sie können Auskunft über Ihre gespeicherten Daten verlangen.
- **Recht auf Berichtigung** (Art. 16 DSGVO): Sie können die Korrektur unrichtiger Daten verlangen.
- **Recht auf Löschung** (Art. 17 DSGVO): Sie können die Löschung Ihrer Daten verlangen, sofern keine gesetzlichen Aufbewahrungspflichten entgegenstehen.
- **Recht auf Einschränkung der Verarbeitung** (Art. 18 DSGVO)
- **Recht auf Datenübertragbarkeit** (Art. 20 DSGVO)
- **Widerspruchsrecht** (Art. 21 DSGVO)

